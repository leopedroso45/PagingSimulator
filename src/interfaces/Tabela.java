    public void alimentarTextTab() {
        List<Pagina> paginas = processo.getPaginas();
        try {
        for (Pagina pagina : paginas) {
            for (Quadro quadro : quadros) {
                if (quadro.getPagina() != null) {
                    if (quadro.getPagina().equals(pagina)) {
                        atualQuadrs.add(quadro);
                    }
                }
            }
        }

        textTab(atualQuadrs);
        atualQuadrs = null;
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "A tabela do processo: " + this.processo.getId() + " n√£o pode ser listada!");
        }
    }

    public void textTab(List<Quadro> quadrs) {
        for (Quadro quadr : quadrs) {
            this.tabelaProcesso.append("Endereco do Quadro: \n" + quadr.getId() +"\nEndereco da Pagina\n"+ quadr.getPagina().getId() + "\n");
        }
        

    }

